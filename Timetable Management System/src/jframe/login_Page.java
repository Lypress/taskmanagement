    public void login(){
        String name = txt_username.getText();
        String pwd = txt_password.getText();
        String User_type = combo_UserType.getSelectedItem().toString();
        
        try {
            Connection con = DBConnection.getConnection();
            PreparedStatement pst = con.prepareStatement("select * from userdetails where name= ? and password= ? and userType = ?");
            
            pst.setString(1, name);
            pst.setString(2,pwd);
            pst.setString(3, User_type);
            
            ResultSet rs = pst.executeQuery();
            
            if (rs.next()) {
                JOptionPane.showMessageDialog(this, "login is successfull you are login as " + rs.getString("userType"));
                
                if (combo_UserType.getSelectedIndex()== 0) {
                    studentHomePage home = new studentHomePage();
                    home.setVisible(true);
                    this.dispose();
                    
                    //String login_name = txt_username.getText();
                    //new studentHomePage(login_name).setVisible(true);
                    
                }
                else if (combo_UserType.getSelectedIndex()== 1) {
                    LecturerHomePage page = new LecturerHomePage();
                    page.setVisible(true);
                    this.dispose(); 
                    
                    //String login_name = txt_username.getText();
                    //new LecturerHomePage(login_name).setVisible(true);
                }
                else{
                    AdminHomePage page = new AdminHomePage();
                    page.setVisible(true);
                    this.dispose();
                    
                    /*String login_name = txt_username.getText();
                    new AdminHomePage(login_name).setVisible(true);*/
                
                }    
            }else{
                JOptionPane.showMessageDialog(this, "Wrong Username or Password or User type");
            }
            
            
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    
    
